//Calculating the size of data types using sizeof()
#include<stdio.h>
int main()
{
	printf("Size of Integer data type: %d byte",sizeof(int));
	printf("\nSize of Character data type: %d byte",sizeof(char));
	printf("\nSize of Float data type: %d byte",sizeof(float));
	printf("\nSize of Double data type: %d byte",sizeof(double));
}
